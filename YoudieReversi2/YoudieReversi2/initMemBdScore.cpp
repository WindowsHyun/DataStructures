void C_REVERSI :: initMemBdScore()
{
	for(int i = 0 ; i < 8 ; i ++ )
	{
		for(int j = 0 ; j < 8 ; j ++ )
		{
			m_arrBdScore[i][j] = 0;
		}
	}


	int tmpScore[8][8] = 
	{
		900,300,400,400,400,400,300,900,
		300, 50,200,200,200,200, 50,300,
		400,200,300,300,300,300,200,400,
		400,200,300,300,300,300,200,400,
		400,200,300,300,300,300,200,400,
		400,200,300,300,300,300,200,400,
		300, 50,200,200,200,200, 50,300,
		900,300,400,400,400,400,300,900,
	};

	for(int i = 0 ; i < 8 ; i ++ )
	{
		for(int j = 0 ; j < 8 ; j ++ )
		{
			if(m_arrBdStat[i][j] == 3)
			{
				m_arrBdScore[i][j] = tmpScore[i][j];
			}
		}
	}
}